"""Stage 1 (FSA-v5): rolling-window SMC2 filter on synthetic data.

Goal of this driver: confirm that the rolling-window SMC2 filter recovers
the ground-truth model parameters from clean synthetic observations
generated by the plant under a fixed control schedule. NO controller is
run -- this isolates `_dynamics`, `_plant`, `estimation`, and the obs
samplers. Bugs in any of those four pieces surface here cheaply, before
the closed-loop bench burns GPU time.

Mirrors `version_2/tools/bench_smc_rolling_window_fsa.py` (FSA-v2 Stage E3)
with these v5 adaptations:

  * 6D latent state [B, S, F, A, K_FB, K_FS] (was 3D [B, F, A]).
  * Bimodal control Phi=(Phi_B, Phi_S) (was scalar Phi).
  * 5 observation channels: HR, sleep, stress, steps, VolumeLoad
    (added VolumeLoad in v5).
  * 37 estimable parameters in `PARAM_PRIOR_CONFIG` per LaTeX guide §7
    (was 30 in v2). All 37 are exercised by the coverage gate; 14 frozen
    parameters are not in scope (LaTeX §7.2 -- by design unidentifiable).

Default scenario: 14 days under fixed Phi=(0.30, 0.30), the v5 healthy
island per LaTeX §8 Test 2 (`A_target ~= 0.55` interior attractor).
Window/stride/replan cadence matches v2 (1d / 12h / -- no replan).

Acceptance gates (initial -- tighten/loosen after first run):

  1. Posterior 90% CI covers truth on >=30/37 estimable params for at
     least 22/27 windows. Loosely matches v2 E3's "5/6 covered for >=24/27
     windows" scaled to 37 params.
  2. Compute <=60 min on RTX 5090.

Run:
    cd version_3 && PYTHONPATH=.:.. python tools/bench_smc_filter_only_fsa_v5.py
    # optional first arg = output suffix (e.g. "g1_no_infoaware")
"""

from __future__ import annotations

import os
os.environ.setdefault('JAX_ENABLE_X64', 'True')

# JAX persistent compilation cache (matches v2 driver pattern)
import pathlib as _pathlib
_CACHE_DIR = _pathlib.Path.home() / ".jax_compilation_cache"
_CACHE_DIR.mkdir(parents=True, exist_ok=True)
os.environ.setdefault('JAX_COMPILATION_CACHE_DIR', str(_CACHE_DIR))
os.environ.setdefault('JAX_PERSISTENT_CACHE_MIN_ENTRY_SIZE_BYTES', '0')
os.environ.setdefault('JAX_PERSISTENT_CACHE_MIN_COMPILE_TIME_SECS', '1')

import sys
import time

import jax
import jax.numpy as jnp
import numpy as np
import matplotlib.pyplot as plt


# ── Window structure (matches v2's E3 reference; LaTeX §6 confirms 15-min grid)
N_DAYS_TOTAL    = 14
WINDOW_BINS     = 96    # 1 day at dt=15 min
STRIDE_BINS     = 48    # 12 hours
DT              = 1.0 / 96.0   # 15 min in days

# ── Stage 1 scenario: healthy-island Phi (LaTeX §8 Test 2).
DEFAULT_PHI_B = 0.30
DEFAULT_PHI_S = 0.30

# Trained-athlete initial state per LaTeX §8 (Test 1 setup, reused by Test 2).
# DEFAULT_INIT is a deconditioned cold-start that collapses A->0 under
# Phi=(0.30, 0.30); the healthy-island demo wants this trained-athlete state
# whose A(100d) > 0.4 per LaTeX Test 2's pass criterion.
TRAINED_ATHLETE_STATE = np.array([0.50, 0.45, 0.20, 0.45, 0.06, 0.07])


def extract_window(obs_data, start: int, end: int):
    """Slice obs_data to bins [start, end), re-indexing t_idx to [0, end-start)."""
    window = {}
    for ch_name, ch_data in obs_data.items():
        t_idx = np.asarray(ch_data['t_idx'])
        mask = (t_idx >= start) & (t_idx < end)
        new_ch = {'t_idx': (t_idx[mask] - start).astype(np.int32)}
        n_t = len(t_idx)
        for key in ch_data:
            if key == 't_idx':
                continue
            val = ch_data[key]
            arr = np.asarray(val)
            if arr.ndim >= 1 and len(arr) == n_t:
                new_ch[key] = arr[mask]
            else:
                new_ch[key] = val
        window[ch_name] = new_ch
    return window


def _simulate_synthetic_full(seed: int = 42,
                              phi_B: float = DEFAULT_PHI_B,
                              phi_S: float = DEFAULT_PHI_S):
    """Use StepwisePlant to synthesise N_DAYS_TOTAL days of constant-Phi data.

    Parameters
    ----------
    phi_B, phi_S : float
        Daily aerobic / strength stimulus amplitudes. Default
        (0.30, 0.30) is the LaTeX §8 Test 2 healthy island.

    Returns
    -------
    dict with keys
        ``obs_data``   -- dict of per-channel obs (5 channels + Phi + C)
        ``trajectory`` -- (N_DAYS_TOTAL*96, 6) latent [B, S, F, A, K_FB, K_FS]
        ``n_bins``     -- int
    """
    from version_3.models.fsa_v5._plant import StepwisePlant
    from version_3.models.fsa_v5.simulation import DEFAULT_PARAMS_V5

    # CRITICAL: v5 plant wants DEFAULT_PARAMS_V5 (includes obs coefs),
    # not TRUTH_PARAMS_V5 (drift+diffusion only). LaTeX §9.5 footgun.
    plant = StepwisePlant(
        truth_params=dict(DEFAULT_PARAMS_V5),
        state=TRAINED_ATHLETE_STATE.copy(),
        seed_offset=seed,
    )
    daily = np.tile([[phi_B, phi_S]], (N_DAYS_TOTAL, 1))
    n_bins = N_DAYS_TOTAL * 96
    out = plant.advance(n_bins, daily)

    # `out` already has global t_idx; collect into the canonical obs_data
    # schema that the filter's align_obs_fn expects.
    obs_data = {
        'obs_HR':         out['obs_HR'],
        'obs_sleep':      out['obs_sleep'],
        'obs_stress':     out['obs_stress'],
        'obs_steps':      out['obs_steps'],
        'obs_volumeload': out['obs_volumeload'],
        'Phi':            out['Phi'],
        'C':              out['C'],
    }
    return {
        'obs_data':   obs_data,
        'trajectory': np.asarray(out['trajectory']),
        'n_bins':     n_bins,
    }


def main():
    # CLI: optional first arg = output suffix (e.g. "g1_no_infoaware",
    # "g2_infoaware"). If suffix contains "infoaware" without "no_",
    # automatically enable sf_info_aware=True. Mirrors v2 E3 convention.
    out_suffix = sys.argv[1] if len(sys.argv) > 1 else ''
    sf_info_aware_on = ('infoaware' in out_suffix
                         and 'no_infoaware' not in out_suffix)

    print("=" * 76)
    print("  Stage 1 (FSA-v5) -- rolling-window SMC2 filter, 14d at "
          f"Phi=({DEFAULT_PHI_B},{DEFAULT_PHI_S})"
          + (f"  [{out_suffix}]" if out_suffix else ''))
    print("=" * 76)

    n_windows = (N_DAYS_TOTAL * 96 - WINDOW_BINS) // STRIDE_BINS + 1
    print(f"  device:     {jax.devices()[0].platform.upper()}")
    print(f"  total:      {N_DAYS_TOTAL} days x 96 bins = {N_DAYS_TOTAL*96} bins")
    print(f"  window:     {WINDOW_BINS} bins (1 day)")
    print(f"  stride:     {STRIDE_BINS} bins (12 hours)")
    print(f"  windows:    {n_windows}")
    print(f"  bridge:     SF Path B-fixed, sf_info_aware={sf_info_aware_on}")
    print()

    print("  Step 1: synthesize 14-day trajectory under "
          f"Phi=({DEFAULT_PHI_B}, {DEFAULT_PHI_S}) [healthy island]")
    data = _simulate_synthetic_full(
        seed=42, phi_B=DEFAULT_PHI_B, phi_S=DEFAULT_PHI_S)
    traj = data['trajectory']
    state_names = ['B', 'S', 'F', 'A', 'K_FB', 'K_FS']
    print(f"    trajectory shape: {traj.shape}")
    for i, name in enumerate(state_names):
        print(f"    {name:5s} range: [{traj[:, i].min():.3f}, "
              f"{traj[:, i].max():.3f}]")
    print()

    from version_3.models.fsa_v5.estimation import (
        HIGH_RES_FSA_V5_ESTIMATION, COLD_START_INIT,
    )
    from version_3.models.fsa_v5.simulation import DEFAULT_PARAMS_V5
    from smc2fc.core.config import SMCConfig
    from smc2fc.core.tempered_smc import run_smc_window, run_smc_window_bridge
    from smc2fc.transforms.unconstrained import unconstrained_to_constrained
    from smc2fc.filtering.gk_dpf_v3_lite import make_gk_dpf_v3_lite_log_density

    em = HIGH_RES_FSA_V5_ESTIMATION
    truth = dict(DEFAULT_PARAMS_V5)

    # Bridge config: same SF Path B-fixed pattern that ships in v2's E3 reference.
    smc_cfg = SMCConfig(
        n_smc_particles=128, n_pf_particles=200,
        target_ess_frac=0.5, max_lambda_inc=0.10,
        bridge_type='schrodinger_follmer',
        sf_q1_mode='annealed',
        sf_use_q0_cov=True,
        sf_blend=0.7,
        sf_annealed_n_stages=3,
        sf_annealed_n_mh_steps=5,
        sf_info_aware=sf_info_aware_on,
        num_mcmc_steps=5, hmc_step_size=0.025, hmc_num_leapfrog=8,
        num_mcmc_steps_bridge=3, max_lambda_inc_bridge=0.15,
    )

    # Stage 1 gate: posterior covers truth at 90% on the FULL 37-param
    # estimable subset (LaTeX §7). Frozen 14 params are by definition not
    # identifiable.
    all_param_names = list(em.all_names)
    identifiable_subset = set(all_param_names)
    name_to_idx = {n: i for i, n in enumerate(all_param_names)}
    n_id = len(identifiable_subset)

    all_results = []
    prev_particles = None
    # Filter's window-1 fixed init state matches the plant's true init
    # (trained-athlete) so the cold-start posterior is well-aligned with
    # the data. Without this, COLD_START_INIT (a deconditioned baseline)
    # would put the prior far from the truth and waste tempering levels
    # on locating the state alone.
    fixed_init_state = jnp.asarray(TRAINED_ATHLETE_STATE)

    total_t0 = time.time()
    for w in range(n_windows):
        start = w * STRIDE_BINS
        end = start + WINDOW_BINS

        print(f"  Window {w+1:>2}/{n_windows}  bins {start:>4}-{end:>4}",
              end='', flush=True)

        window_obs = extract_window(data['obs_data'], start, end)
        n_obs_w = (len(window_obs['obs_HR']['t_idx'])
                    + len(window_obs['obs_stress']['t_idx'])
                    + len(window_obs['obs_steps']['t_idx'])
                    + len(window_obs['obs_volumeload']['t_idx'])
                    + len(window_obs['obs_sleep']['t_idx']))
        print(f"  obs={n_obs_w:>3} ", end='', flush=True)

        grid_obs = em.align_obs_fn(window_obs, WINDOW_BINS, DT)
        ld = make_gk_dpf_v3_lite_log_density(
            model=em, grid_obs=grid_obs, n_particles=smc_cfg.n_pf_particles,
            bandwidth_scale=smc_cfg.bandwidth_scale,
            ot_ess_frac=smc_cfg.ot_ess_frac, ot_temperature=smc_cfg.ot_temperature,
            ot_max_weight=smc_cfg.ot_max_weight,
            ot_rank=smc_cfg.ot_rank, ot_n_iter=smc_cfg.ot_n_iter,
            ot_epsilon=smc_cfg.ot_epsilon,
            dt=DT, seed=42 + w,
            fixed_init_state=fixed_init_state, window_start_bin=int(start),
        )
        T_arr = ld._transforms

        if prev_particles is None:
            init_tag = 'cold'
            particles, elapsed, n_temp = run_smc_window(
                ld, em, T_arr, cfg=smc_cfg,
                initial_particles=None, seed=42 + w * 1000,
            )
        else:
            init_tag = 'bridge'
            particles, elapsed, n_temp = run_smc_window_bridge(
                new_ld=ld, prev_particles=prev_particles,
                model=em, T_arr=T_arr, cfg=smc_cfg,
                seed=42 + w * 1000,
            )
        print(f"({init_tag}) {n_temp:>2} levels, {elapsed:>5.1f}s ",
              end='', flush=True)

        # Convert to constrained parameter space
        particles = np.asarray(particles)
        samp = np.array([
            np.asarray(unconstrained_to_constrained(jnp.asarray(p), T_arr))
            for p in particles
        ])

        # Coverage on identifiable subset
        n_id_covered = 0
        for name in identifiable_subset:
            idx = name_to_idx[name]
            q05 = np.quantile(samp[:, idx], 0.05)
            q95 = np.quantile(samp[:, idx], 0.95)
            if q05 <= truth[name] <= q95:
                n_id_covered += 1
        print(f" id_cov={n_id_covered}/{n_id}", flush=True)

        all_results.append({
            'window': w,
            'start_bin': start, 'end_bin': end,
            'n_temp': n_temp, 'elapsed_s': elapsed,
            'init_tag': init_tag,
            'particles_constrained': samp,
            'id_covered': n_id_covered,
        })
        prev_particles = particles

        # Smoothed init state at t=STRIDE_BINS for next window's PF
        n_extract = min(10, particles.shape[0])
        extracted = []
        for ei in range(n_extract):
            u_draw = jnp.array(particles[ei])
            st = ld.extract_state_at_step(u_draw, STRIDE_BINS)
            extracted.append(np.array(st))
        fixed_init_state = jnp.array(np.mean(extracted, axis=0))

    total_elapsed = time.time() - total_t0
    print()
    print(f"  Total: {total_elapsed/60:.1f} min ({total_elapsed:.0f}s) for "
          f"{n_windows} windows")

    # ── Acceptance gates ──
    # Threshold for "covered enough": >=80% of params (>=30/37).
    cov_thresh = int(np.ceil(0.80 * n_id))
    n_pass = sum(1 for r in all_results if r['id_covered'] >= cov_thresh)
    pass_thresh = int(np.ceil(0.80 * n_windows))
    print()
    print(f"  Acceptance gates:")
    gate1 = n_pass >= pass_thresh
    print(f"    {'PASS' if gate1 else 'FAIL'}  >={pass_thresh}/{n_windows} windows "
          f"have >={cov_thresh}/{n_id} identifiable covered "
          f"(actual: {n_pass}/{n_windows})")
    gate2 = total_elapsed <= 60 * 60
    print(f"    {'PASS' if gate2 else 'FAIL'}  Total compute <=60 min "
          f"(actual: {total_elapsed:.0f}s)")
    all_pass = gate1 and gate2
    print(f"  {'ALL GATES PASS' if all_pass else 'ONE OR MORE GATES FAIL'}")

    # ── Diagnostic plots ──
    out_dir = "outputs/fsa_v5"
    os.makedirs(out_dir, exist_ok=True)
    base_path = (f"{out_dir}/Stage1_filter_only_traces"
                  + (f"_{out_suffix}" if out_suffix else ''))

    # Plot 1: ALL 37 estimable params in a 5x8 grid (or whatever fits)
    n_params_all = len(all_param_names)
    n_cols = 6
    n_rows = (n_params_all + n_cols - 1) // n_cols
    fig, axes = plt.subplots(n_rows, n_cols, figsize=(20, 3 * n_rows))
    axes = axes.flatten()
    x = np.arange(n_windows)
    for i, name in enumerate(all_param_names):
        ax = axes[i]
        means = [r['particles_constrained'][:, i].mean() for r in all_results]
        q05s  = [np.quantile(r['particles_constrained'][:, i], 0.05)
                 for r in all_results]
        q95s  = [np.quantile(r['particles_constrained'][:, i], 0.95)
                 for r in all_results]
        # Coverage indicator: was THIS param covered in each window?
        per_window_cov = []
        for r in all_results:
            q05 = np.quantile(r['particles_constrained'][:, i], 0.05)
            q95 = np.quantile(r['particles_constrained'][:, i], 0.95)
            per_window_cov.append(q05 <= truth[name] <= q95)
        cov_frac = np.mean(per_window_cov)
        color = 'steelblue' if cov_frac >= 0.5 else 'darkorange'
        ax.plot(x, means, 'o-', color=color, markersize=2, lw=0.8)
        ax.fill_between(x, q05s, q95s, alpha=0.25, color=color)
        truth_val = truth.get(name, None)
        if truth_val is not None:
            ax.axhline(truth_val, color='red', linestyle='--', lw=0.8)
        ax.set_title(f'{name}  cov={cov_frac:.0%}', fontsize=9)
        ax.tick_params(labelsize=7)
        ax.grid(True, alpha=0.3)
    for i in range(n_params_all, len(axes)):
        axes[i].set_visible(False)
    plt.suptitle(
        f'Stage 1 (FSA-v5) -- 27-window rolling-SMC2 posterior trace'
        + (f' [{out_suffix}]' if out_suffix else '')
        + f': {n_pass}/{n_windows} windows >={cov_thresh}/{n_id} covered, '
        + f'{total_elapsed/60:.0f} min on {jax.devices()[0].platform.upper()}',
        fontsize=12, y=1.0)
    plt.tight_layout()
    plt.savefig(f"{base_path}.png", dpi=120)
    plt.close()

    print()
    print(f"  Plot: {base_path}.png  (all {n_params_all} estimable params)")
    print("=" * 76)


if __name__ == '__main__':
    main()
