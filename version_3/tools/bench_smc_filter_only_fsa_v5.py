def _pop_run_tag_from_argv(default: str) -> str:
    if '--run-tag' in sys.argv:
        i = sys.argv.index('--run-tag')
        if i + 1 >= len(sys.argv):
            raise SystemExit("--run-tag requires a value")
        val = sys.argv[i + 1]
        del sys.argv[i:i + 2]
        return val
    return default


def _next_run_number(experiments_dir: Path) -> int:
    if not experiments_dir.exists():
        return 1
    nums = []
    for p in experiments_dir.iterdir():
        if p.is_dir() and p.name.startswith('run'):
            stem = p.name[3:].split('_', 1)[0]
            try:
                nums.append(int(stem))
            except ValueError:
                pass
    return max(nums, default=0) + 1


def _allocate_run_dir(repo_root: Path, run_tag: str) -> tuple[Path, int]:
    """Make outputs/fsa_v5/experiments/runNN_<tag>/ and return (path, NN)."""
    exp_dir = repo_root / "outputs" / "fsa_v5" / "experiments"
    exp_dir.mkdir(parents=True, exist_ok=True)
    n = _next_run_number(exp_dir)
    out_dir = exp_dir / f"run{n:02d}_{run_tag}"
    out_dir.mkdir(exist_ok=True)
    return out_dir, n


# ── Window structure (matches v2's E3 reference; LaTeX §6 confirms 15-min grid)
N_DAYS_TOTAL    = 14
WINDOW_BINS     = 96    # 1 day at dt=15 min
STRIDE_BINS     = 48    # 12 hours
DT              = 1.0 / 96.0   # 15 min in days

# ── Stage 1 scenario: healthy-island Phi (LaTeX §8 Test 2).
DEFAULT_PHI_B = 0.30
DEFAULT_PHI_S = 0.30

# Trained-athlete initial state per LaTeX §8 (Test 1 setup, reused by Test 2).
# DEFAULT_INIT is a deconditioned cold-start that collapses A->0 under
# Phi=(0.30, 0.30); the healthy-island demo wants this trained-athlete state
# whose A(100d) > 0.4 per LaTeX Test 2's pass criterion.
TRAINED_ATHLETE_STATE = np.array([0.50, 0.45, 0.20, 0.45, 0.06, 0.07])


def extract_window(obs_data, start: int, end: int):
    """Slice obs_data to bins [start, end), re-indexing t_idx to [0, end-start)."""
    window = {}
    for ch_name, ch_data in obs_data.items():
        t_idx = np.asarray(ch_data['t_idx'])
        mask = (t_idx >= start) & (t_idx < end)
        new_ch = {'t_idx': (t_idx[mask] - start).astype(np.int32)}
        n_t = len(t_idx)
        for key in ch_data:
            if key == 't_idx':
                continue
            val = ch_data[key]
            arr = np.asarray(val)
            if arr.ndim >= 1 and len(arr) == n_t:
                new_ch[key] = arr[mask]
            else:
                new_ch[key] = val
        window[ch_name] = new_ch
    return window


def _simulate_synthetic_full(seed: int = 42,
                              phi_B: float = DEFAULT_PHI_B,
                              phi_S: float = DEFAULT_PHI_S):
    """Use StepwisePlant to synthesise N_DAYS_TOTAL days of constant-Phi data.

    Parameters
    ----------
    phi_B, phi_S : float
        Daily aerobic / strength stimulus amplitudes. Default
        (0.30, 0.30) is the LaTeX §8 Test 2 healthy island.

    Returns
    -------
    dict with keys
        ``obs_data``   -- dict of per-channel obs (5 channels + Phi + C)
        ``trajectory`` -- (N_DAYS_TOTAL*96, 6) latent [B, S, F, A, K_FB, K_FS]
        ``n_bins``     -- int
    """
    from version_3.models.fsa_v5._plant import StepwisePlant
    from version_3.models.fsa_v5.simulation import DEFAULT_PARAMS_V5

    # CRITICAL: v5 plant wants DEFAULT_PARAMS_V5 (includes obs coefs),
    # not TRUTH_PARAMS_V5 (drift+diffusion only). LaTeX §9.5 footgun.
    plant = StepwisePlant(
        truth_params=dict(DEFAULT_PARAMS_V5),
        state=TRAINED_ATHLETE_STATE.copy(),
        seed_offset=seed,
    )
    daily = np.tile([[phi_B, phi_S]], (N_DAYS_TOTAL, 1))
    n_bins = N_DAYS_TOTAL * 96
    out = plant.advance(n_bins, daily)

    # `out` already has global t_idx; collect into the canonical obs_data
    # schema that the filter's align_obs_fn expects.
    obs_data = {
        'obs_HR':         out['obs_HR'],
        'obs_sleep':      out['obs_sleep'],
        'obs_stress':     out['obs_stress'],
        'obs_steps':      out['obs_steps'],
        'obs_volumeload': out['obs_volumeload'],
        'Phi':            out['Phi'],
        'C':              out['C'],
    }
    return {
        'obs_data':   obs_data,
        'trajectory': np.asarray(out['trajectory']),
        'n_bins':     n_bins,
    }


def main():
    # CLI:
    #   --run-tag <str>      override the auto-tag for the experiment folder.
    #                         Default = "stage1_filter_only_T14_healthy".
    #   <suffix>             optional positional arg, propagated into plots.
    #                         "infoaware" enables sf_info_aware=True.
    run_tag = _pop_run_tag_from_argv("stage1_filter_only_T14_healthy")
    out_suffix = sys.argv[1] if len(sys.argv) > 1 else ''
    sf_info_aware_on = ('infoaware' in out_suffix
                         and 'no_infoaware' not in out_suffix)

    repo_root = Path(__file__).resolve().parent.parent  # version_3/
    out_dir, run_num = _allocate_run_dir(repo_root, run_tag)

    print("=" * 76)
    print("  Stage 1 (FSA-v5) -- rolling-window SMC2 filter, 14d at "
          f"Phi=({DEFAULT_PHI_B},{DEFAULT_PHI_S})"
          + (f"  [{out_suffix}]" if out_suffix else ''))
    print(f"  run dir:  {out_dir.relative_to(repo_root.parent)}")
    print("=" * 76)

    n_windows = (N_DAYS_TOTAL * 96 - WINDOW_BINS) // STRIDE_BINS + 1
    print(f"  device:     {jax.devices()[0].platform.upper()}")
    print(f"  total:      {N_DAYS_TOTAL} days x 96 bins = {N_DAYS_TOTAL*96} bins")
    print(f"  window:     {WINDOW_BINS} bins (1 day)")
    print(f"  stride:     {STRIDE_BINS} bins (12 hours)")
    print(f"  windows:    {n_windows}")
    print(f"  bridge:     SF Path B-fixed, sf_info_aware={sf_info_aware_on}")
    print()

    print("  Step 1: synthesize 14-day trajectory under "
          f"Phi=({DEFAULT_PHI_B}, {DEFAULT_PHI_S}) [healthy island]")
    data = _simulate_synthetic_full(
        seed=42, phi_B=DEFAULT_PHI_B, phi_S=DEFAULT_PHI_S)
    traj = data['trajectory']
    state_names = ['B', 'S', 'F', 'A', 'K_FB', 'K_FS']
    print(f"    trajectory shape: {traj.shape}")
    for i, name in enumerate(state_names):
        print(f"    {name:5s} range: [{traj[:, i].min():.3f}, "
              f"{traj[:, i].max():.3f}]")
    print()

    from version_3.models.fsa_v5.estimation import (
        HIGH_RES_FSA_V5_ESTIMATION, COLD_START_INIT,
    )
    from version_3.models.fsa_v5.simulation import DEFAULT_PARAMS_V5
    from smc2fc.core.config import SMCConfig
    # v2-production native path: compile-once factory + Partial-wrapped per-stride
    # log_density. Avoids the per-window JIT recompile that the BlackJAX path
    # paid (and that left the GPU idle most of the time). Mirrors
    # version_2/tools/bench_smc_full_mpc_fsa.py:150-260,317-337.
    from smc2fc.core.jax_native_smc import (
        run_smc_window_native, run_smc_window_bridge_native,
    )
    from smc2fc.transforms.unconstrained import unconstrained_to_constrained
    from smc2fc.filtering.gk_dpf_v3_lite import (
        make_gk_dpf_v3_lite_log_density_compileonce,
    )

    em = HIGH_RES_FSA_V5_ESTIMATION
    truth = dict(DEFAULT_PARAMS_V5)

    # 5090-saturation point per CLAUDE.md "GPU saturated post-driver
    # update": "the GPU saturates at N=256/K=400 post-driver-update.
    # Doubling N_SMC_PARTICLES is no longer 'free'." So 256/400 is the
    # right operating point for verification: GPU saturated AND wall-
    # clock manageable. v2 production uses 1024/800 for a tighter
    # posterior at the cost of ~16x wall-clock; we don't need that for
    # Stage 1 gate-checking.
    smc_cfg = SMCConfig(
        n_smc_particles=256, n_pf_particles=400,
        target_ess_frac=0.5, max_lambda_inc=0.10,
        bridge_type='schrodinger_follmer',
        sf_q1_mode='annealed',
        sf_use_q0_cov=True,
        sf_blend=0.7,
        sf_annealed_n_stages=3,
        sf_annealed_n_mh_steps=5,
        sf_info_aware=sf_info_aware_on,
        num_mcmc_steps=5, hmc_step_size=0.025, hmc_num_leapfrog=8,
        num_mcmc_steps_bridge=3, max_lambda_inc_bridge=0.15,
    )

    # Stage 1 gate: posterior covers truth at 90% on the FULL 37-param
    # estimable subset (LaTeX §7). Frozen 14 params are by definition not
    # identifiable.
    all_param_names = list(em.all_names)
    identifiable_subset = set(all_param_names)
    name_to_idx = {n: i for i, n in enumerate(all_param_names)}
    n_id = len(identifiable_subset)

    # ── Build the compile-once log-density factory ONCE before the loop ──
    # The factory takes per-stride dynamic data (grid_obs, fixed_init_state,
    # w_start, key0) as ARGUMENTS; per-stride we wrap it in jax.tree_util.Partial
    # so JAX's JIT cache hits across strides. v2 production pattern from
    # `bench_smc_full_mpc_fsa.py:250-260`.
    log_density_factory = make_gk_dpf_v3_lite_log_density_compileonce(
        model=em, n_particles=smc_cfg.n_pf_particles,
        bandwidth_scale=smc_cfg.bandwidth_scale,
        ot_ess_frac=smc_cfg.ot_ess_frac,
        ot_temperature=smc_cfg.ot_temperature,
        ot_max_weight=smc_cfg.ot_max_weight,
        ot_rank=smc_cfg.ot_rank, ot_n_iter=smc_cfg.ot_n_iter,
        ot_epsilon=smc_cfg.ot_epsilon,
        dt=DT, t_steps=WINDOW_BINS,
    )
    T_arr = log_density_factory._transforms

    all_results = []
    prev_particles = None
    # Filter's window-1 fixed init state matches the plant's true init
    # (trained-athlete) so the cold-start posterior is well-aligned with
    # the data. Without this, COLD_START_INIT (a deconditioned baseline)
    # would put the prior far from the truth and waste tempering levels
    # on locating the state alone.
    fixed_init_state = jnp.asarray(TRAINED_ATHLETE_STATE)

    total_t0 = time.time()
    for w in range(n_windows):
        start = w * STRIDE_BINS
        end = start + WINDOW_BINS

        print(f"  Window {w+1:>2}/{n_windows}  bins {start:>4}-{end:>4}",
              end='', flush=True)

        window_obs = extract_window(data['obs_data'], start, end)
        n_obs_w = (len(window_obs['obs_HR']['t_idx'])
                    + len(window_obs['obs_stress']['t_idx'])
                    + len(window_obs['obs_steps']['t_idx'])
                    + len(window_obs['obs_volumeload']['t_idx'])
                    + len(window_obs['obs_sleep']['t_idx']))
        print(f"  obs={n_obs_w:>3} ", end='', flush=True)

        grid_obs = em.align_obs_fn(window_obs, WINDOW_BINS, DT)

        # Per-stride: bind dynamic data to the compile-once factory via
        # jax.tree_util.Partial. The Partial is a stable JAX pytree -> the
        # underlying jitted function's cache hits across strides (no
        # per-window recompile of the SDE scan).
        key0_stride = jax.random.PRNGKey(42 + w)
        w_start_arr = jnp.asarray(start, dtype=jnp.int32)
        ld = jax.tree_util.Partial(
            log_density_factory,
            grid_obs=grid_obs,
            fixed_init_state=fixed_init_state,
            w_start=w_start_arr,
            key0=key0_stride,
        )

        if prev_particles is None:
            init_tag = 'cold'
            particles, elapsed, n_temp = run_smc_window_native(
                ld, em, T_arr, cfg=smc_cfg,
                initial_particles=None, seed=42 + w * 1000,
            )
        else:
            init_tag = 'bridge'
            particles, elapsed, n_temp = run_smc_window_bridge_native(
                new_ld=ld, prev_particles=prev_particles,
                model=em, T_arr=T_arr, cfg=smc_cfg,
                seed=42 + w * 1000,
            )
        print(f"({init_tag}) {n_temp:>2} levels, {elapsed:>5.1f}s ",
              end='', flush=True)

        # Convert to constrained parameter space
        particles = np.asarray(particles)
        samp = np.array([
            np.asarray(unconstrained_to_constrained(jnp.asarray(p), T_arr))
            for p in particles
        ])

        # Coverage on identifiable subset
        n_id_covered = 0
        for name in identifiable_subset:
            idx = name_to_idx[name]
            q05 = np.quantile(samp[:, idx], 0.05)
            q95 = np.quantile(samp[:, idx], 0.95)
            if q05 <= truth[name] <= q95:
                n_id_covered += 1
        print(f" id_cov={n_id_covered}/{n_id}", flush=True)

        all_results.append({
            'window': w,
            'start_bin': start, 'end_bin': end,
            'n_temp': n_temp, 'elapsed_s': elapsed,
            'init_tag': init_tag,
            'particles_constrained': samp,
            'id_covered': n_id_covered,
        })
        prev_particles = particles

        # Smoothed end-of-stride state for next window's PF init.
        # vmap-batched extract via the compile-once factory (v2 production
        # pattern from bench_smc_full_mpc_fsa.py:354-367).
        n_extract = min(10, particles.shape[0])
        us_extract = jnp.asarray(particles[:n_extract])
        target_step_arr = jnp.asarray(STRIDE_BINS, dtype=jnp.int32)
        extract_partial = jax.tree_util.Partial(
            log_density_factory.extract_state_at_step,
            grid_obs=grid_obs,
            fixed_init_state=fixed_init_state,
            w_start=w_start_arr,
            key0=key0_stride,
            target_step=target_step_arr,
        )
        states = jax.vmap(extract_partial)(us_extract)
        fixed_init_state = jnp.asarray(jnp.mean(states, axis=0))

    total_elapsed = time.time() - total_t0
    print()
    print(f"  Total: {total_elapsed/60:.1f} min ({total_elapsed:.0f}s) for "
          f"{n_windows} windows")

    # ── Acceptance gates ──
    # Threshold for "covered enough": >=80% of params (>=30/37).
    cov_thresh = int(np.ceil(0.80 * n_id))
    n_pass = sum(1 for r in all_results if r['id_covered'] >= cov_thresh)
    pass_thresh = int(np.ceil(0.80 * n_windows))
    print()
    print(f"  Acceptance gates:")
    gate1 = n_pass >= pass_thresh
    print(f"    {'PASS' if gate1 else 'FAIL'}  >={pass_thresh}/{n_windows} windows "
          f"have >={cov_thresh}/{n_id} identifiable covered "
          f"(actual: {n_pass}/{n_windows})")
    gate2 = total_elapsed <= 60 * 60
    print(f"    {'PASS' if gate2 else 'FAIL'}  Total compute <=60 min "
          f"(actual: {total_elapsed:.0f}s)")
    all_pass = gate1 and gate2
    print(f"  {'ALL GATES PASS' if all_pass else 'ONE OR MORE GATES FAIL'}")

    # ── Save reproducible artifacts into out_dir ──
    n_params_all = len(all_param_names)
    posterior_array = np.stack([
        r['particles_constrained'] for r in all_results
    ])  # (n_windows, n_smc_particles, n_params)
    elapsed_per_window = np.array([r['elapsed_s'] for r in all_results])
    id_covered_per_window = np.array([r['id_covered'] for r in all_results])
    n_temp_per_window = np.array([r['n_temp'] for r in all_results])
    truth_vec = np.array([truth[n] for n in all_param_names])

    np.savez(
        out_dir / "posterior.npz",
        posterior=posterior_array,
        param_names=np.array(all_param_names, dtype=object),
        truth_vec=truth_vec,
        elapsed_s=elapsed_per_window,
        id_covered=id_covered_per_window,
        n_temp=n_temp_per_window,
    )

    np.savez(
        out_dir / "trajectory.npz",
        trajectory=traj,
        state_names=np.array(state_names, dtype=object),
    )

    # Manifest -- mirrors version_2/outputs/swat/experiments/runNN/manifest.json
    manifest = {
        "schema_version": 1,
        "stage": 1,
        "bench": "bench_smc_filter_only_fsa_v5",
        "run_tag": run_tag,
        "run_number": run_num,
        "out_suffix": out_suffix,
        "fsa_model_dev_pin": "7075436628fa8c202cf62241666fe90230c46ac1",
        "T_total_days": N_DAYS_TOTAL,
        "step_minutes": 15,
        "BINS_PER_DAY": 96,
        "WINDOW_BINS": WINDOW_BINS,
        "STRIDE_BINS": STRIDE_BINS,
        "DT": DT,
        "n_windows": n_windows,
        "scenario": {
            "name": "healthy_island_LaTeX_test2",
            "phi_B": DEFAULT_PHI_B,
            "phi_S": DEFAULT_PHI_S,
            "init_state": TRAINED_ATHLETE_STATE.tolist(),
            "init_state_label": "trained_athlete_LaTeX_test1",
        },
        "param_names": all_param_names,
        "n_estimable_params": n_id,
        "truth_params": {k: float(v) for k, v in truth.items()},
        "smc_cfg": {
            "n_smc_particles": smc_cfg.n_smc_particles,
            "n_pf_particles": smc_cfg.n_pf_particles,
            "target_ess_frac": smc_cfg.target_ess_frac,
            "max_lambda_inc": smc_cfg.max_lambda_inc,
            "bridge_type": smc_cfg.bridge_type,
            "sf_q1_mode": smc_cfg.sf_q1_mode,
            "sf_use_q0_cov": smc_cfg.sf_use_q0_cov,
            "sf_blend": smc_cfg.sf_blend,
            "sf_annealed_n_stages": smc_cfg.sf_annealed_n_stages,
            "sf_annealed_n_mh_steps": smc_cfg.sf_annealed_n_mh_steps,
            "sf_info_aware": smc_cfg.sf_info_aware,
            "num_mcmc_steps": smc_cfg.num_mcmc_steps,
            "hmc_step_size": smc_cfg.hmc_step_size,
            "hmc_num_leapfrog": smc_cfg.hmc_num_leapfrog,
            "num_mcmc_steps_bridge": smc_cfg.num_mcmc_steps_bridge,
            "max_lambda_inc_bridge": smc_cfg.max_lambda_inc_bridge,
        },
        "summary": {
            "total_compute_s": total_elapsed,
            "total_compute_min": total_elapsed / 60.0,
            "device": jax.devices()[0].platform.upper(),
            "n_windows_pass_id_cov": n_pass,
            "id_cov_threshold": cov_thresh,
            "pass_threshold_windows": pass_thresh,
            "trajectory_summary": {
                f"{name}_final": float(traj[-1, i])
                for i, name in enumerate(state_names)
            },
            "gates": {
                f"id_cov_geq_{cov_thresh}_for_geq_{pass_thresh}_windows": gate1,
                "compute_leq_60min": gate2,
                "all_pass": all_pass,
            },
        },
    }
    with open(out_dir / "manifest.json", 'w') as f:
        json.dump(manifest, f, indent=2)

    # ── Diagnostic plots ──
    base_name = "Stage1_filter_only_traces" + (f"_{out_suffix}" if out_suffix else '')

    # Plot 1: all 37 estimable params -- posterior 90% CI per window vs truth
    n_cols = 6
    n_rows = (n_params_all + n_cols - 1) // n_cols
    fig, axes = plt.subplots(n_rows, n_cols, figsize=(20, 3 * n_rows))
    axes = axes.flatten()
    x = np.arange(n_windows)
    per_param_cov_frac = []
    for i, name in enumerate(all_param_names):
        ax = axes[i]
        means = [r['particles_constrained'][:, i].mean() for r in all_results]
        q05s  = [np.quantile(r['particles_constrained'][:, i], 0.05)
                 for r in all_results]
        q95s  = [np.quantile(r['particles_constrained'][:, i], 0.95)
                 for r in all_results]
        per_window_cov = [
            (np.quantile(r['particles_constrained'][:, i], 0.05) <= truth[name]
             <= np.quantile(r['particles_constrained'][:, i], 0.95))
            for r in all_results
        ]
        cov_frac = float(np.mean(per_window_cov))
        per_param_cov_frac.append(cov_frac)
        color = 'steelblue' if cov_frac >= 0.5 else 'darkorange'
        ax.plot(x, means, 'o-', color=color, markersize=2, lw=0.8)
        ax.fill_between(x, q05s, q95s, alpha=0.25, color=color)
        ax.axhline(truth[name], color='red', linestyle='--', lw=0.8)
        ax.set_title(f'{name}  cov={cov_frac:.0%}', fontsize=9)
        ax.tick_params(labelsize=7)
        ax.grid(True, alpha=0.3)
    for i in range(n_params_all, len(axes)):
        axes[i].set_visible(False)
    plt.suptitle(
        f'Stage 1 (FSA-v5) -- 27-window rolling-SMC2 posterior trace'
        + (f' [{out_suffix}]' if out_suffix else '')
        + f': {n_pass}/{n_windows} windows >={cov_thresh}/{n_id} covered, '
        + f'{total_elapsed/60:.0f} min on {jax.devices()[0].platform.upper()}',
        fontsize=12, y=1.0)
    plt.tight_layout()
    plt.savefig(out_dir / f"{base_name}.png", dpi=120)
    plt.close()

    # Plot 2: latent trajectory (6 states + 5 obs channels-ish)
    fig, axes = plt.subplots(2, 3, figsize=(15, 8))
    axes = axes.flatten()
    t_days = np.arange(traj.shape[0]) * DT
    for i, name in enumerate(state_names):
        ax = axes[i]
        ax.plot(t_days, traj[:, i], color='steelblue', lw=1.0)
        ax.set_xlabel('days')
        ax.set_ylabel(name)
        ax.set_title(f'{name} (truth simulator)')
        ax.grid(True, alpha=0.3)
    plt.suptitle(
        f'Stage 1 (FSA-v5) -- 14-day truth trajectory at '
        f'Phi=({DEFAULT_PHI_B}, {DEFAULT_PHI_S})',
        fontsize=12, y=1.0)
    plt.tight_layout()
    plt.savefig(out_dir / "latent_trajectory.png", dpi=120)
    plt.close()

    # Persist per-param coverage fractions back into the manifest summary.
    manifest["summary"]["per_param_coverage_frac"] = {
        name: per_param_cov_frac[i] for i, name in enumerate(all_param_names)
    }
    with open(out_dir / "manifest.json", 'w') as f:
        json.dump(manifest, f, indent=2)

